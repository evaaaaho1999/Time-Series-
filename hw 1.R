#(a)
w<-rnorm(150,0,1)
x<-filter(w,filter=c(0,-.9), method="recursive")[-(1:50)]
v<-filter(x, rep(1/4,4),sides = 1)
plot.ts(x)
lines(v,lty=2)
#(b)
x2<-cos(2*pi*1:100/4)
v<-filter(x2, rep(1/4,4),sides = 1)
plot.ts(x2, main=expression(cos(2*pi*1:100/4)))
lines(v,lty=2)
#(c)
x2<-cos(2*pi*1:100/4)
w<-rnorm(100,0,1)
v<-filter(x2+w, rep(1/4,4),sides = 1)
plot.ts(x2+w, main=expression(w+cos(2*pi*1:100/4)))
lines(v,lty=2)
